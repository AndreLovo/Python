#ESTRUTURA DE REPETIÇÃO FOR (LAÇOS DE REPETIÇÃO)
#LAÇO COM VARIÁVEL DE CONTROLE

for c in range(0, 6):
    print('Oi')
print('FIM')
#CUIDADO COM A INDENTAÇÃO
for c in range(0, 6):
    print('Oi')
    print('==FIM==')

for c in range(1,7):
    print(c)
print('FIM')

#CUIDADO COM A ITERAÇÃO (CONTAR PARA TRÁS)
for c in range(6,0,-1):
    print(c)
print('FIM')

#(CONTANDO PARA TRÁS DE 2 EM 2)
for c in range(7,0,-2):
    print(c)
print('======FIM=======')
for c in range(0,7,2):
    print(c)
print('======FIM=======')
for c in range(7,0,2):
    print(c)
print('======FIM=======')